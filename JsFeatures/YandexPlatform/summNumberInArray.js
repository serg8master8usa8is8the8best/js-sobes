// Напишите функцию, которая принимает на вход два массива arr1 и arr2, представляющих числа, и возвращает их сумму в виде массива.
// - Каждый элемент массивов arr1 и arr2 представляет одну цифру числа.
// - Результат также должен быть представлен в виде массива, где каждый элемент - одна цифра.
// - Если в результате есть ведущие нули, они должны быть сохранены.
// Примеры:
// Input 1: [1, 2, 3], [4, 5, 6]
// Output 1: [5, 7, 9]
// Input 2: [5, 4, 4], [4, 5, 6]
// Output 2: [1, 0, 0, 0]

// создаем 2 указателя по длинне массива 

// создаем лишнюю сумму

// while 1 и 2 указатели больше или равны 0

// если массив закончился, то подставляем в значение ноль, иначе берем из массива 

// суммируем 1 2 и лишнюю сумму 

// если больше 10, то забираем только body и добавляем в массив, а лишнюю сумму сохраняем 

// если что-то осталось, то пушим в перенос 

// разварачиваем массив 

function addNumbers(arr1, arr2) {
    let pointer1 = arr1.length - 1;
    let pointer2 = arr2.length - 1;
    let result = [];
    let carry = 0; // перенос

    // Обрабатываем оба массива, пока есть элементы в любом из них
    while (pointer1 >= 0 || pointer2 >= 0) {
        // Берем цифры или 0, если массив закончился
        const digit1 = pointer1 >= 0 ? arr1[pointer1] : 0;
        const digit2 = pointer2 >= 0 ? arr2[pointer2] : 0;

        // Суммируем с учетом переноса
        let sum = digit1 + digit2 + carry;

        // Обновляем перенос
        carry = Math.floor(sum / 10);

        // Записываем только последнюю цифру
        result.push(sum % 10);

        // Двигаем указатели
        pointer1--;
        pointer2--;
    }

    // Если после всех операций остался перенос
    if (carry > 0) {
        result.push(carry);
    }

    // Переворачиваем массив и возвращаем
    return result.reverse();
};

// Input: [1], [9, 9, 9]
// Output: [1,0,0,0]